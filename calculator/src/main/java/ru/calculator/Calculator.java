package ru.calculator;

public class Calculator {
    public static int sum(int a, int b) {
        return a + b;
    }

    public static int min(int a, int b) {
        return a - b;
    }

    public static int multiple(int a, int b) {
        return a * b;
    }

    public static double dib(int a, int b) {
        return a / (b * 1D);
    }
}
